# Add System.Web for HtmlEncode
Add-Type -AssemblyName System.Web -ErrorAction SilentlyContinue

Write-Host ""
Write-Host "========================================" -ForegroundColor Cyan
Write-Host " Win32 AppWorkload Log Analyzer" -ForegroundColor Cyan
Write-Host "========================================" -ForegroundColor Cyan
Write-Host ""

# --- Step 1: Discover log files ---
$filesToProcess = @()

if ($LogFiles) {
    foreach ($f in $LogFiles) {
        if (Test-Path $f) {
            $filesToProcess += (Resolve-Path $f).Path
        }
        else {
            Write-Warning "File not found: $f"
        }
    }
}
elseif ($LogFolder) {
    if (Test-Path $LogFolder) {
        $discovered = @(Get-ChildItem -Path $LogFolder -File | Where-Object { $_.Name -match 'AppWorkload.*\.log$' } | Select-Object -ExpandProperty FullName)
        if ($discovered.Count -gt 0) {
            $filesToProcess = $discovered
            Write-Host "Discovered $($filesToProcess.Count) log file(s) in folder." -ForegroundColor Green
        }
        else {
            Write-Error "No AppWorkload*.log files found in: $LogFolder"
            return
        }
    }
    else {
        Write-Error "Folder not found: $LogFolder"
        return
    }
}
else {
    # Default: try local IME log folder
    $defaultPath = "$env:ProgramData\Microsoft\IntuneManagementExtension\Logs"
    if (Test-Path $defaultPath) {
        $discovered = @(Get-ChildItem -Path $defaultPath -File | Where-Object { $_.Name -match 'AppWorkload.*\.log$' } | Select-Object -ExpandProperty FullName)
        if ($discovered.Count -gt 0) {
            $filesToProcess = $discovered
            Write-Host "Auto-discovered $($filesToProcess.Count) log file(s) from default IME path." -ForegroundColor Green
        }
    }

    if ($filesToProcess.Count -eq 0) {
        Write-Host ""
        Write-Host "Usage:" -ForegroundColor Yellow
        Write-Host "  .\Analyze-Win32AppWorkload.ps1 -LogFolder <path>"
        Write-Host "  .\Analyze-Win32AppWorkload.ps1 -LogFiles <file1>,<file2>"
        Write-Host "  .\Analyze-Win32AppWorkload.ps1 -LogFolder <path> -ShowInBrowser"
        Write-Host ""
        Write-Host "Or run on an Intune-managed device to auto-discover logs from:"
        Write-Host "  $defaultPath" -ForegroundColor Cyan
        return
    }
}

if ($filesToProcess.Count -eq 0) {
    Write-Error "No valid log files to process."
    return
}

Write-Host ""
Write-Host "[Step 1/5] Merging $($filesToProcess.Count) log file(s)..." -ForegroundColor Yellow
$logFileNames = $filesToProcess | ForEach-Object { Split-Path $_ -Leaf }

# --- Step 2: Merge and Parse ---
$rawLines = Merge-LogFiles -FilePaths $filesToProcess
Write-Host "  Total lines: $($rawLines.Count)" -ForegroundColor Gray

Write-Host "[Step 2/5] Parsing log entries..." -ForegroundColor Yellow
$logEntries = @(Parse-CMTraceLog -LogContent $rawLines)
Write-Host "  Parsed entries: $($logEntries.Count)" -ForegroundColor Gray

if ($logEntries.Count -eq 0) {
    Write-Error "No valid log entries parsed. Check if the files are in CMTrace format."
    return
}

# Date range
$sortedEntries = $logEntries | Where-Object { $_.DateTime -ne [DateTime]::MinValue } | Sort-Object DateTime
$dateRange = ""
if ($sortedEntries.Count -gt 0) {
    $firstDate = ($sortedEntries | Select-Object -First 1).DateTime.ToString('yyyy-MM-dd HH:mm:ss')
    $lastDate = ($sortedEntries | Select-Object -Last 1).DateTime.ToString('yyyy-MM-dd HH:mm:ss')
    $dateRange = "$firstDate  ~  $lastDate"
    Write-Host "  Date range: $dateRange" -ForegroundColor Gray
}

# --- Step 3: Extract App Inventory ---
Write-Host "[Step 3/5] Extracting app inventory from policies..." -ForegroundColor Yellow
$appInventory = Parse-PolicyJSON -LogEntries $logEntries

if ($appInventory.Count -eq 0) {
    Write-Error "No apps found in 'Get policies' JSON. The log may not contain policy data."
    return
}

Write-Host "  Found $($appInventory.Count) app(s) in policy." -ForegroundColor Gray

# --- Step 4: Extract per-app events ---
Write-Host "[Step 4/5] Extracting per-app events and states..." -ForegroundColor Yellow
$espEvents = @(Get-EspPhaseEvents -LogEntries $logEntries)
$checkinSessions = @(Get-CheckinSessions -LogEntries $logEntries)
Get-AppEvents -LogEntries $logEntries -AppInventory $appInventory -EspPhaseEvents $espEvents

$allErrors = @(Get-AllErrors -LogEntries $logEntries -AppInventory $appInventory)

# Summary
$detected = @($appInventory.Values | Where-Object { $_.DetectionState -eq 'Detected' }).Count
$notDetected = @($appInventory.Values | Where-Object { $_.DetectionState -eq 'NotDetected' }).Count
$noActivity = @($appInventory.Values | Where-Object { -not $_.DetectionState }).Count
Write-Host "  Detected: $detected | Not detected: $notDetected | No activity: $noActivity" -ForegroundColor Gray
$allErrors = @($allErrors)
Write-Host "  Errors found: $($allErrors.Count)" -ForegroundColor $(if ($allErrors.Count -gt 0) { 'Red' } else { 'Gray' })

# --- Step 5: Generate HTML Report ---
Write-Host "[Step 5/5] Generating HTML report..." -ForegroundColor Yellow

$htmlContent = Generate-HtmlReport -AppInventory $appInventory -LogFileNames $logFileNames -LogDateRange $dateRange -AllErrors $allErrors -CheckinSessions $checkinSessions -EspPhaseEvents $espEvents

# Determine output path
if (-not $OutputPath) {
    $firstFileDir = Split-Path $filesToProcess[0] -Parent
    $OutputPath = Join-Path $firstFileDir "Win32AppWorkload_Report.html"
}

# Handle directory in OutputPath
if (Test-Path $OutputPath -PathType Container) {
    $OutputPath = Join-Path $OutputPath "Win32AppWorkload_Report.html"
}

# Ensure output directory exists
$outputDir = Split-Path $OutputPath -Parent
if (-not (Test-Path $outputDir)) {
    New-Item -ItemType Directory -Path $outputDir -Force | Out-Null
}

$htmlContent | Out-File -FilePath $OutputPath -Encoding UTF8 -Force
Write-Host ""
Write-Host "Report generated successfully!" -ForegroundColor Green
Write-Host "  Output: $OutputPath" -ForegroundColor Cyan
Write-Host ""

if ($ShowInBrowser) {
    Write-Host "Opening in browser..." -ForegroundColor Gray
    Start-Process $OutputPath
}
