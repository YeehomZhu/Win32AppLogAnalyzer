function Get-ReportJavaScript {
    return @'
function filterApps() {
    const searchText = document.getElementById('searchBox').value.toLowerCase();
    const statusFilter = document.getElementById('statusFilter').value;
    const intentFilter = document.getElementById('intentFilter').value;
    const rows = document.querySelectorAll('.app-row');

    rows.forEach(row => {
        const name = row.getAttribute('data-name').toLowerCase();
        const status = row.getAttribute('data-status');
        const intent = row.getAttribute('data-intent');
        const detailRow = document.getElementById('detail-' + row.getAttribute('data-appid'));

        let show = true;
        if (searchText && !name.includes(searchText)) show = false;
        if (statusFilter && statusFilter !== 'all' && status !== statusFilter) show = false;
        if (intentFilter && intentFilter !== 'all' && intent !== intentFilter) show = false;

        row.style.display = show ? '' : 'none';
        if (detailRow) detailRow.style.display = 'none';
        if (detailRow) detailRow.classList.remove('active');
    });
}

function toggleDetail(appId) {
    const detailRow = document.getElementById('detail-' + appId);
    if (detailRow) {
        detailRow.classList.toggle('active');
        detailRow.style.display = detailRow.classList.contains('active') ? 'table-row' : 'none';
    }
}

function toggleAllDetails() {
    const detailRows = document.querySelectorAll('.detail-row');
    const anyHidden = Array.from(detailRows).some(r => !r.classList.contains('active'));

    detailRows.forEach(r => {
        if (anyHidden) {
            r.classList.add('active');
            r.style.display = 'table-row';
        } else {
            r.classList.remove('active');
            r.style.display = 'none';
        }
    });
}

let sortDirection = {};

function sortTable(colIndex) {
    const table = document.getElementById('appTable');
    const tbody = table.querySelector('tbody');
    const rows = Array.from(tbody.querySelectorAll('tr.app-row'));

    sortDirection[colIndex] = !sortDirection[colIndex];
    const dir = sortDirection[colIndex] ? 1 : -1;

    rows.sort((a, b) => {
        const aVal = a.cells[colIndex].textContent.trim().toLowerCase();
        const bVal = b.cells[colIndex].textContent.trim().toLowerCase();
        if (!isNaN(aVal) && !isNaN(bVal)) return (parseFloat(aVal) - parseFloat(bVal)) * dir;
        return aVal.localeCompare(bVal) * dir;
    });

    rows.forEach(row => {
        const detailRow = document.getElementById('detail-' + row.getAttribute('data-appid'));
        tbody.appendChild(row);
        if (detailRow) tbody.appendChild(detailRow);
    });

    // Update sort icons
    const headers = table.querySelectorAll('th');
    headers.forEach((h, i) => {
        const icon = h.querySelector('.sort-icon');
        if (icon) icon.textContent = i === colIndex ? (sortDirection[colIndex] ? ' ▲' : ' ▼') : '';
    });
}

function exportCSV() {
    const table = document.getElementById('appTable');
    const rows = table.querySelectorAll('tr.app-row');
    const headers = Array.from(table.querySelectorAll('th')).map(h => h.textContent.replace(/[▲▼]/g, '').trim());

    let csv = headers.join(',') + '\n';
    rows.forEach(row => {
        const cells = Array.from(row.cells).map(c => '"' + c.textContent.trim().replace(/"/g, '""') + '"');
        csv += cells.join(',') + '\n';
    });

    const blob = new Blob(['\ufeff' + csv], { type: 'text/csv;charset=utf-8;' });
    const link = document.createElement('a');
    link.href = URL.createObjectURL(blob);
    link.download = 'Win32AppWorkload_Report.csv';
    link.click();
}

function toggleSection(sectionId) {
    const content = document.getElementById(sectionId);
    if (content) {
        content.style.display = content.style.display === 'none' ? '' : 'none';
    }
}

