function Get-AppEvents {
    <#
    .SYNOPSIS
        For each app, search the log for all relevant events and populate event data.
    #>
    param(
        [Parameter(Mandatory)]
        [PSCustomObject[]]$LogEntries,

        [Parameter(Mandatory)]
        [hashtable]$AppInventory,

        [Parameter(Mandatory)]
        [PSCustomObject[]]$EspPhaseEvents
    )

    foreach ($appId in $AppInventory.Keys) {
        $app = $AppInventory[$appId]
        $timeline = [System.Collections.Generic.List[PSCustomObject]]::new()
        $errors = [System.Collections.Generic.List[PSCustomObject]]::new()

        # Filter log entries mentioning this AppId
        $appEntries = $LogEntries | Where-Object { $_.Message -match [regex]::Escape($appId) }

        # --- Detection State ---
        $detectionEntries = $appEntries | Where-Object {
            $_.Message -match '\[DetectionActionHandler\].*Detection for policy with id:.*resulted in action status:'
        }
        if ($detectionEntries) {
            $lastDetection = $detectionEntries | Select-Object -Last 1
            if ($lastDetection.Message -match 'action status:\s*(\w+)\s*and detection state:\s*(\w+)') {
                $app.DetectionStatus = $Matches[1]
                $app.DetectionState = $Matches[2]
            }
            foreach ($d in $detectionEntries) {
                $timeline.Add([PSCustomObject]@{ Time = $d.DateTime; Event = 'Detection'; Detail = $d.Message; Type = $d.Type })
            }
        }

        # --- Applicability ---
        $applicabilityEntries = $appEntries | Where-Object {
            $_.Message -match '\[ApplicabilityActionHandler\].*Applicability check for policy with id:.*resulted in'
        }
        if ($applicabilityEntries) {
            $lastApplic = $applicabilityEntries | Select-Object -Last 1
            if ($lastApplic.Message -match 'applicability state:\s*(\w+)') {
                $app.ApplicabilityState = $Matches[1]
            }
            foreach ($a in $applicabilityEntries) {
                $timeline.Add([PSCustomObject]@{ Time = $a.DateTime; Event = 'Applicability'; Detail = $a.Message; Type = $a.Type })
            }
        }

        # --- Download ---
        $downloadStartEntries = $appEntries | Where-Object {
            $_.Message -match '\[DownloadActionHandler\] Handler invoked for policy with id:'
        }
        if ($downloadStartEntries) {
            $lastDlStart = $downloadStartEntries | Select-Object -Last 1
            $app.DownloadTimestamp = $lastDlStart.DateTime
            $timeline.Add([PSCustomObject]@{ Time = $lastDlStart.DateTime; Event = 'Download Start'; Detail = $lastDlStart.Message; Type = $lastDlStart.Type })
        }

        $downloadCompleteEntries = $appEntries | Where-Object {
            $_.Message -match '\[ContentManagerV2\].*Completed.*content download.*result status:'
        }
        if ($downloadCompleteEntries) {
            $lastDlComplete = $downloadCompleteEntries | Select-Object -Last 1
            if ($lastDlComplete.Message -match 'result status:\s*(\w+)') {
                $app.DownloadStatus = $Matches[1]
            }
            $timeline.Add([PSCustomObject]@{ Time = $lastDlComplete.DateTime; Event = 'Download Complete'; Detail = $lastDlComplete.Message; Type = $lastDlComplete.Type })
        }

        # --- Install Start ---
        $installStartEntries = $appEntries | Where-Object {
            $_.Message -match '\[ExecutionActionHandler\] Handler invoked with execution type:\s*Install\s*for policy with id:'
        }
        if ($installStartEntries) {
            $lastInstStart = $installStartEntries | Select-Object -Last 1
            $app.InstallStartTime = $lastInstStart.DateTime
            $timeline.Add([PSCustomObject]@{ Time = $lastInstStart.DateTime; Event = 'Install Start'; Detail = $lastInstStart.Message; Type = $lastInstStart.Type })
        }

        # --- Install Exit Code & Result ---
        # Find lpExitCode entries after the install start for this app
        if ($app.InstallStartTime) {
            $postInstallEntries = $LogEntries | Where-Object {
                $_.DateTime -ge $app.InstallStartTime -and $_.DateTime -le $app.InstallStartTime.AddMinutes(120)
            }

            $exitCodeEntry = $postInstallEntries | Where-Object { $_.Message -match '^lpExitCode\s+(-?\d+)$' } | Select-Object -First 1
            if ($exitCodeEntry) {
                if ($exitCodeEntry.Message -match 'lpExitCode\s+(-?\d+)') {
                    $app.InstallExitCode = $Matches[1]
                }
                $timeline.Add([PSCustomObject]@{ Time = $exitCodeEntry.DateTime; Event = 'Exit Code'; Detail = $exitCodeEntry.Message; Type = $exitCodeEntry.Type })
            }

            $resultEntry = $postInstallEntries | Where-Object { $_.Message -match 'lpExitCode is defined as\s+(\w+)' } | Select-Object -First 1
            if ($resultEntry) {
                if ($resultEntry.Message -match 'lpExitCode is defined as\s+(\w+)') {
                    $app.InstallResult = $Matches[1]
                }
                $timeline.Add([PSCustomObject]@{ Time = $resultEntry.DateTime; Event = 'Install Result'; Detail = $resultEntry.Message; Type = $resultEntry.Type })
            }
        }

        # --- Install Complete ---
        $installCompleteEntries = $appEntries | Where-Object {
            $_.Message -match '\[ExecutionActionHandler\] Handler completed with action status:'
        }
        if ($installCompleteEntries) {
            $lastInstComplete = $installCompleteEntries | Select-Object -Last 1
            $app.InstallCompleteTime = $lastInstComplete.DateTime
            $timeline.Add([PSCustomObject]@{ Time = $lastInstComplete.DateTime; Event = 'Install Complete'; Detail = $lastInstComplete.Message; Type = $lastInstComplete.Type })
        }

        # --- Post-Install Detection ---
        $postDetEntries = $appEntries | Where-Object {
            $_.Message -match '\[DetectionActionHandler\].*Policy with id:.*is expected to have enforcement state:\s*Detected'
        }
        if ($postDetEntries) {
            $timeline.Add([PSCustomObject]@{ Time = ($postDetEntries | Select-Object -Last 1).DateTime; Event = 'Post-Install Detection Expected'; Detail = ($postDetEntries | Select-Object -Last 1).Message; Type = 1 })
        }

        # --- Enforcement State Tracking ---
        $enforcementEntries = $appEntries | Where-Object {
            $_.Message -match '\[ReportingManager\].*Download state for app with id:.*EnforcementState'
        }
        $enfStates = @()
        foreach ($e in $enforcementEntries) {
            if ($e.Message -match '"NewValue":"?([^",}]+)"?') {
                $enfStates += [PSCustomObject]@{ Time = $e.DateTime; State = $Matches[1] }
                $timeline.Add([PSCustomObject]@{ Time = $e.DateTime; Event = 'Enforcement State'; Detail = "EnforcementState -> $($Matches[1])"; Type = $e.Type })
            }
        }
        $app.EnforcementStates = $enfStates
        if ($enfStates.Count -gt 0) {
            $app.LatestEnforcementState = ($enfStates | Select-Object -Last 1).State
        }

        # --- Errors ---
        $errorEntries = $appEntries | Where-Object { $_.Type -eq 3 }
        foreach ($err in $errorEntries) {
            $errors.Add([PSCustomObject]@{
                Time    = $err.DateTime
                Message = $err.Message
                AppId   = $appId
                AppName = $app.Name
            })
            $timeline.Add([PSCustomObject]@{ Time = $err.DateTime; Event = 'Error'; Detail = $err.Message; Type = 3 })
        }

        # --- ESP Phase ---
        if ($app.InstallStartTime -and $EspPhaseEvents.Count -gt 0) {
            $app.EspPhase = Get-EspPhaseAtTime -EspPhaseEvents $EspPhaseEvents -TargetTime $app.InstallStartTime
        }
        elseif ($EspPhaseEvents.Count -gt 0) {
            $app.EspPhase = ($EspPhaseEvents | Select-Object -Last 1).Phase
        }

        # Sort timeline
        $app.EventTimeline = $timeline | Sort-Object Time
        $app.Errors = $errors
    }
}

function Get-EspPhaseAtTime {
    param(
        [PSCustomObject[]]$EspPhaseEvents,
        [DateTime]$TargetTime
    )

    $before = $EspPhaseEvents | Where-Object { $_.Time -le $TargetTime } | Select-Object -Last 1
    if ($before) { return $before.Phase }
    return 'Unknown'
}

function Get-EspPhaseEvents {
    param([PSCustomObject[]]$LogEntries)

    $espEvents = [System.Collections.Generic.List[PSCustomObject]]::new()
    foreach ($entry in $LogEntries) {
        if ($entry.Message -match 'The EspPhase:\s*(\w+)' -or $entry.Message -match 'EspPhase:\s*(\w+)') {
            $espEvents.Add([PSCustomObject]@{
                Time  = $entry.DateTime
                Phase = $Matches[1]
            })
        }
    }
    return $espEvents.ToArray()
}

function Get-CheckinSessions {
    param([PSCustomObject[]]$LogEntries)

    $sessions = [System.Collections.Generic.List[PSCustomObject]]::new()
    $sessionStart = $null

    foreach ($entry in $LogEntries) {
        if ($entry.Message -match '\[Win32AppAsync\] Starting app check in' -or
            $entry.Message -match '----- application poller starts\. -----') {
            $sessionStart = $entry.DateTime
        }

        if ($sessionStart -and $entry.Message -match 'Got (\d+) active user sessions') {
            $sessions.Add([PSCustomObject]@{
                StartTime    = $sessionStart
                UserSessions = [int]$Matches[1]
            })
        }
    }
    return $sessions.ToArray()
}

function Get-AllErrors {
    param(
        [PSCustomObject[]]$LogEntries,
        [hashtable]$AppInventory
    )

    $appIds = @($AppInventory.Keys)
    $generalErrors = [System.Collections.Generic.List[PSCustomObject]]::new()

    foreach ($entry in ($LogEntries | Where-Object { $_.Type -eq 3 })) {
        # Check if this error mentions any known AppId
        $isAppSpecific = $false
        foreach ($id in $appIds) {
            if ($entry.Message -match [regex]::Escape($id)) {
                $isAppSpecific = $true
                break
            }
        }
        if (-not $isAppSpecific) {
            $generalErrors.Add([PSCustomObject]@{
                Time    = $entry.DateTime
                Message = $entry.Message
            })
        }
    }
    return $generalErrors.ToArray()
}

