<#
.SYNOPSIS
    Win32 AppWorkload Log Analyzer - Parses Intune AppWorkload.log files and generates an HTML report.

.DESCRIPTION
    This tool parses Microsoft Intune's AppWorkload.log files (generated by the Intune Management Extension / SideCar agent)
    and produces a visual HTML report summarizing all Win32 app deployments on a device.

.PARAMETER LogFiles
    One or more AppWorkload.log file paths.

.PARAMETER LogFolder
    Folder path to auto-discover all AppWorkload*.log files.

.PARAMETER OutputPath
    Output path for the HTML report. Defaults to the same folder as the first log file.

.PARAMETER ShowInBrowser
    Auto-open the report in the default browser after generation.

.EXAMPLE
    .\Analyze-Win32AppWorkload.ps1 -LogFolder "C:\ProgramData\Microsoft\IntuneManagementExtension\Logs"
    .\Analyze-Win32AppWorkload.ps1 -LogFiles "C:\Logs\AppWorkload.log" -ShowInBrowser
#>

[CmdletBinding()]
param(
    [Parameter(Mandatory = $false)]
    [string[]]$LogFiles,

    [Parameter(Mandatory = $false)]
    [string]$LogFolder,

    [Parameter(Mandatory = $false)]
    [string]$OutputPath,

    [Parameter(Mandatory = $false)]
    [switch]$ShowInBrowser
)

Set-StrictMode -Version Latest
$ErrorActionPreference = 'Continue'

