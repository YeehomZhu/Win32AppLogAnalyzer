            $html += "                <div class=`"info-item`"><span class=`"label`">Max Runtime:</span><span class=`"value`">$($ib.MaxRunTimeInMinutes) min</span></div>`n"
            $html += "                <div class=`"info-item`"><span class=`"label`">Restart Behavior:</span><span class=`"value`">$($ib.DeviceRestartBehavior)</span></div>`n"
            $html += "                <div class=`"info-item`"><span class=`"label`">Requires Logon:</span><span class=`"value`">$($ib.RequiresLogon)</span></div>`n"
            $html += "            </div>`n"
        }

        # Detail - Return Codes
        if ($app.ReturnCodes -and @($app.ReturnCodes).Count -gt 0) {
            $html += "            <h4>Return Codes</h4>`n"
            $html += "            <table class=`"return-codes-table`"><tr><th>Code</th><th>Type</th></tr>`n"
            foreach ($rc in $app.ReturnCodes) {
                $html += "            <tr><td>$($rc.ReturnCode)</td><td>$([System.Web.HttpUtility]::HtmlEncode($rc.TypeName))</td></tr>`n"
            }
            $html += "            </table>`n"
        }

        # Detail - Dependencies
        if ($app.Dependencies) {
            $html += "            <h4>Dependencies</h4>`n"
            $html += "            <div class=`"code-block`">$([System.Web.HttpUtility]::HtmlEncode(($app.Dependencies | ConvertTo-Json -Compress)))</div>`n"
        }

        # Detail - Event Timeline (deduplicated: show first & last per event type, plus state changes)
        if ($app.EventTimeline -and @($app.EventTimeline).Count -gt 0) {
            $html += "            <h4>Event Timeline</h4>`n"
            $html += "            <ul class=`"timeline-list`">`n"

            # Group events by type, keep first/last and any with different Detail
            $sortedEvents = @($app.EventTimeline | Sort-Object Time)
            $grouped = @{}
            foreach ($evt in $sortedEvents) {
                $key = $evt.Event
                if (-not $grouped.ContainsKey($key)) { $grouped[$key] = [System.Collections.Generic.List[PSCustomObject]]::new() }
                $grouped[$key].Add($evt)
            }

            $filteredEvents = [System.Collections.Generic.List[PSCustomObject]]::new()
            foreach ($key in $grouped.Keys) {
                $evts = $grouped[$key]
                if ($evts.Count -le 2) {
                    # 1-2 events: keep all
                    foreach ($e in $evts) { $filteredEvents.Add($e) }
                }
                else {
                    # Keep first
                    $filteredEvents.Add($evts[0])
                    # Keep any middle events where Detail changed from previous
                    for ($ei = 1; $ei -lt $evts.Count - 1; $ei++) {
                        if ($evts[$ei].Detail -ne $evts[$ei - 1].Detail) {
                            $filteredEvents.Add($evts[$ei])
                        }
                    }
                    # Keep last (if different from the one just added)
                    $lastEvt = $evts[$evts.Count - 1]
                    $filteredEvents.Add($lastEvt)
                }
            }

            $filteredEvents = @($filteredEvents | Sort-Object Time)
            foreach ($evt in $filteredEvents) {
                $evtClass = switch ($evt.Type) {
                    3 { 'error-event' }
                    2 { 'warning-event' }
                    default { '' }
                }
                $timeStr = if ($evt.Time -and $evt.Time -ne [DateTime]::MinValue) { $evt.Time.ToString('HH:mm:ss.fff') } else { '??:??:??' }
                $dateStr = if ($evt.Time -and $evt.Time -ne [DateTime]::MinValue) { $evt.Time.ToString('yyyy-MM-dd') } else { '' }
                $html += "                <li class=`"$evtClass`"><span class=`"time`">$dateStr $timeStr</span><span class=`"event-type`">$([System.Web.HttpUtility]::HtmlEncode($evt.Event))</span> $([System.Web.HttpUtility]::HtmlEncode($evt.Detail))</li>`n"
            }
            $html += "            </ul>`n"
        }

        # Detail - Errors
        if ($app.Errors -and @($app.Errors).Count -gt 0) {
            $html += "            <h4>Error Details</h4>`n"
            foreach ($err in $app.Errors) {
                $errTimeStr = if ($err.Time) { $err.Time.ToString('yyyy-MM-dd HH:mm:ss') } else { '' }
                $html += "            <div style=`"color:var(--error);margin-bottom:4px;font-size:12px;`">[$errTimeStr] $([System.Web.HttpUtility]::HtmlEncode($err.Message))</div>`n"
            }
        }

        $html += @"
            </div>
        </td>
    </tr>
"@
    }

    $html += @"
    </tbody>
    </table>
    </div>
</div>
"@

    # ERROR SUMMARY SECTION (general errors only, deduplicated)
    if ($AllErrors -and @($AllErrors).Count -gt 0) {
        # Deduplicate: group by Message, keep first/last and any where Message changed
        $sortedErrors = @($AllErrors | Sort-Object Time)
        $groupedErrors = @{}
        foreach ($err in $sortedErrors) {
            $key = $err.Message
            if (-not $groupedErrors.ContainsKey($key)) { $groupedErrors[$key] = [System.Collections.Generic.List[PSCustomObject]]::new() }
            $groupedErrors[$key].Add($err)
        }
        $filteredErrors = [System.Collections.Generic.List[PSCustomObject]]::new()
        foreach ($key in $groupedErrors.Keys) {
            $errs = $groupedErrors[$key]
            if ($errs.Count -le 2) {
                foreach ($e in $errs) { $filteredErrors.Add($e) }
            }
            else {
                $filteredErrors.Add($errs[0])
                $filteredErrors.Add($errs[$errs.Count - 1])
            }
        }
        $filteredErrors = @($filteredErrors | Sort-Object Time)

        $html += @"

<!-- ERROR SUMMARY -->
<div class="section">
    <div class="section-header" onclick="toggleSection('errorSummaryContent')">
        Error Summary ($($filteredErrors.Count) unique errors from $(@($AllErrors).Count) total)
        <span class="toggle">Click to toggle</span>
    </div>
    <div id="errorSummaryContent">
    <table class="error-summary-table">
    <thead>
        <tr><th>Time</th><th>Message</th></tr>
    </thead>
    <tbody>
"@
        foreach ($err in $filteredErrors) {
            $errTime = if ($err.Time) { $err.Time.ToString('yyyy-MM-dd HH:mm:ss') } else { '' }
            $html += "    <tr><td style=`"white-space:nowrap`">$errTime</td><td class=`"error-msg`">$([System.Web.HttpUtility]::HtmlEncode($err.Message))</td></tr>`n"
        }

        $html += @"
    </tbody>
    </table>
    </div>
</div>
"@
    }

    # CHECKIN SESSIONS

    if ($CheckinSessions -and @($CheckinSessions).Count -gt 0) {
        $html += @"
<div class="section">
    <div class="section-header" onclick="toggleSection('checkinContent')">
        Check-in Sessions ($(@($CheckinSessions).Count) sessions detected)
        <span class="toggle">Click to toggle</span>
    </div>
    <div id="checkinContent" style="display:none">
    <table class="error-summary-table">
    <thead>
        <tr><th>#</th><th>Start Time</th><th>Active User Sessions</th></tr>
    </thead>
    <tbody>
"@
        $i = 1
        foreach ($session in $CheckinSessions) {
            $sessionTime = if ($session.StartTime) { $session.StartTime.ToString('yyyy-MM-dd HH:mm:ss') } else { '' }
            $html += "    <tr><td>$i</td><td>$sessionTime</td><td>$($session.UserSessions)</td></tr>`n"
            $i++
        }
        $html += @"
    </tbody>
    </table>
    </div>
</div>
"@
    }

    # Footer
    $html += @"

<div style="text-align:center;padding:24px;color:var(--text-secondary);font-size:12px;">
    Win32 AppWorkload Log Analyzer | Generated by PowerShell | $(Get-Date -Format 'yyyy')
</div>

</div> <!-- /container -->

<script>
$js
</script>
</body>
</html>
"@

    return $html
}

