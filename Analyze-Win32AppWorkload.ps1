function Parse-PolicyJSON {
    <#
    .SYNOPSIS
        Extract the latest "Get policies" JSON and build an app inventory.
    #>
    param(
        [Parameter(Mandatory)]
        [PSCustomObject[]]$LogEntries
    )

    $policyEntries = @($LogEntries | Where-Object { $_.Message -match '^Get policies = \[' })

    if ($policyEntries.Count -eq 0) {
        Write-Warning "No 'Get policies' line found in the log."
        return @{}
    }

    # Take the LAST occurrence that has actual app data (non-empty JSON array)
    # Some check-in sessions may return "Get policies = []" (empty)
    $lastPolicy = $null
    $apps = $null
    for ($i = $policyEntries.Count - 1; $i -ge 0; $i--) {
        $candidate = $policyEntries[$i]
        $candidateJson = $candidate.Message -replace '^Get policies = ', ''
        if ($candidateJson -and $candidateJson -ne '[]') {
            try {
                $parsedApps = $candidateJson | ConvertFrom-Json
                if ($parsedApps -and @($parsedApps).Count -gt 0) {
                    $lastPolicy = $candidate
                    $apps = $parsedApps
                    break
                }
            }
            catch { continue }
        }
    }

    if (-not $apps) {
        Write-Warning "All 'Get policies' entries contain empty app lists."
        return @{}
    }

    Write-Host "  Using policy entry with $(@($apps).Count) app(s) from $($lastPolicy.DateTime)" -ForegroundColor Gray

    $inventory = [ordered]@{}
    foreach ($app in $apps) {
        $appId = $app.Id
        $inventory[$appId] = [PSCustomObject]@{
            AppId              = $appId
            Name               = $app.Name
            Intent             = $app.Intent
            IntentName         = if ($IntentMap.ContainsKey([int]$app.Intent)) { $IntentMap[[int]$app.Intent] } else { "Unknown($($app.Intent))" }
            TargetType         = $app.TargetType
            TargetTypeName     = if ($TargetTypeMap.ContainsKey([int]$app.TargetType)) { $TargetTypeMap[[int]$app.TargetType] } else { "Unknown($($app.TargetType))" }
            InstallContext     = $app.InstallContext
            InstallContextName = if ($InstallContextMap.ContainsKey([int]$app.InstallContext)) { $InstallContextMap[[int]$app.InstallContext] } else { "Unknown($($app.InstallContext))" }
            InstallCommandLine = $app.InstallCommandLine
            UninstallCommandLine = $app.UninstallCommandLine
            DetectionRules     = Parse-DetectionRules -DetectionRuleJson $app.DetectionRule
            InstallBehavior    = Parse-InstallEx -InstallExJson $app.InstallEx
            ReturnCodes        = Parse-ReturnCodes -ReturnCodesJson $app.ReturnCodes
            ToastState         = if ($ToastStateMap.ContainsKey([int]$app.ToastState)) { $ToastStateMap[[int]$app.ToastState] } else { "$($app.ToastState)" }
            ReevaluationInterval = $app.ReevaluationInterval
            DOPriority         = $app.DOPriority
            Dependencies       = $app.FlatDependencies
            Version            = $app.Version
            # Event tracking - populated later
            DetectionState     = $null
            DetectionStatus    = $null
            ApplicabilityState = $null
            DownloadStatus     = $null
            DownloadTimestamp  = $null
            InstallStartTime   = $null
            InstallExitCode    = $null
            InstallResult      = $null
            InstallCompleteTime = $null
            PostInstallDetection = $null
            EnforcementStates  = @()
            LatestEnforcementState = $null
            EspPhase           = $null
            Errors             = @()
            EventTimeline      = @()
            CheckinSession     = $null
        }
    }

    return $inventory
}

function Parse-DetectionRules {
    param([string]$DetectionRuleJson)

    if (-not $DetectionRuleJson) { return @() }

    $rules = @()
    try {
        $ruleArray = $DetectionRuleJson | ConvertFrom-Json
    }
    catch { return @("Failed to parse detection rules") }

    foreach ($rule in $ruleArray) {
        $outerType = [int]$rule.DetectionType
        $outerTypeName = if ($DetectionTypeOuterMap.ContainsKey($outerType)) { $DetectionTypeOuterMap[$outerType] } else { "Type $outerType" }

        if (-not $rule.DetectionText) {
            $rules += "$outerTypeName (no details)"
            continue
        }

        try {
            $inner = $rule.DetectionText | ConvertFrom-Json
        }
        catch {
            $rules += "$outerTypeName (parse error)"
            continue
        }

        switch ($outerType) {
            0 {
                # Registry
                $innerType = if ($DetectionTypeInnerRegMap.ContainsKey([int]$inner.DetectionType)) { $DetectionTypeInnerRegMap[[int]$inner.DetectionType] } else { "Type $($inner.DetectionType)" }
                $op = if ($OperatorMap.ContainsKey([int]$inner.Operator)) { $OperatorMap[[int]$inner.Operator] } else { "Op $($inner.Operator)" }
                $ruleDesc = "Registry: $($inner.Path)\$($inner.KeyName) | $innerType | $op"
                if ($inner.DetectionValue) { $ruleDesc += " | Expected: $($inner.DetectionValue)" }
                $rules += $ruleDesc
            }
            2 {
                # File/Folder
                $innerType = if ($DetectionTypeInnerFileMap.ContainsKey([int]$inner.DetectionType)) { $DetectionTypeInnerFileMap[[int]$inner.DetectionType] } else { "Type $($inner.DetectionType)" }
                $op = if ($OperatorMap.ContainsKey([int]$inner.Operator)) { $OperatorMap[[int]$inner.Operator] } else { "Op $($inner.Operator)" }
                $ruleDesc = "File: $($inner.Path)\$($inner.FileOrFolderName) | $innerType | $op"
                if ($inner.DetectionValue) { $ruleDesc += " | Expected: $($inner.DetectionValue)" }
                $rules += $ruleDesc
            }
            3 {
                # Script
                $rules += "PowerShell Script Detection (EnforceSignatureCheck: $($inner.EnforceSignatureCheck), RunAs32Bit: $($inner.RunAs32Bit))"
            }
            1 {
                # MSI
                $rules += "MSI Product Code: $($inner.ProductCode)"
            }
            default {
                $rules += "$outerTypeName (details available)"
            }
        }
    }

    return $rules
}

function Parse-InstallEx {
    param([string]$InstallExJson)

    if (-not $InstallExJson) { return $null }

    try {
        $obj = $InstallExJson | ConvertFrom-Json
        return [PSCustomObject]@{
            RunAs                   = if ($RunAsMap.ContainsKey([int]$obj.RunAs)) { $RunAsMap[[int]$obj.RunAs] } else { "$($obj.RunAs)" }
            RequiresLogon           = $obj.RequiresLogon
            MaxRetries              = $obj.MaxRetries
            RetryIntervalInMinutes  = $obj.RetryIntervalInMinutes
            MaxRunTimeInMinutes     = $obj.MaxRunTimeInMinutes
            DeviceRestartBehavior   = if ($DeviceRestartBehaviorMap.ContainsKey([int]$obj.DeviceRestartBehavior)) { $DeviceRestartBehaviorMap[[int]$obj.DeviceRestartBehavior] } else { "Unknown($($obj.DeviceRestartBehavior))" }
            InstallProgramVisibility = $obj.InstallProgramVisibility
        }
    }
    catch { return $null }
}

function Parse-ReturnCodes {
    param([string]$ReturnCodesJson)

    if (-not $ReturnCodesJson) { return @() }

    try {
        $codes = $ReturnCodesJson | ConvertFrom-Json
        return $codes | ForEach-Object {
            [PSCustomObject]@{
                ReturnCode = $_.ReturnCode
                TypeName   = if ($ReturnCodeTypeMap.ContainsKey([int]$_.Type)) { $ReturnCodeTypeMap[[int]$_.Type] } else { "Unknown($($_.Type))" }
            }
        }
    }
    catch { return @() }
}

