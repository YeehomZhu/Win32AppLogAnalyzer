function Parse-CMTraceLog {
    <#
    .SYNOPSIS
        Parse CMTrace/SCCM format log lines into structured objects.
    #>
    param(
        [Parameter(Mandatory)]
        [string[]]$LogContent
    )

    $pattern = '<!\[LOG\[(?<Message>.*?)\]LOG\]!><time="(?<Time>[\d:.]+)" date="(?<Date>[\d-]+)" component="(?<Component>\w*)" context="(?<Context>[^"]*)" type="(?<Type>\d)" thread="(?<Thread>\d+)" file="(?<File>[^"]*)">'

    $results = [System.Collections.Generic.List[PSCustomObject]]::new()

    foreach ($line in $LogContent) {
        if ($line -match $pattern) {
            $dateStr = $Matches['Date']
            $timeStr = $Matches['Time']
            $dt = $null
            try {
                $dt = [DateTime]::ParseExact("$dateStr $timeStr", "M-d-yyyy HH:mm:ss.fffffff", $null)
            }
            catch {
                try {
                    $dt = [DateTime]::ParseExact("$dateStr $timeStr", "M-d-yyyy HH:mm:ss.fff", $null)
                }
                catch {
                    try { $dt = [DateTime]::Parse("$dateStr $timeStr") } catch { $dt = [DateTime]::MinValue }
                }
            }

            $results.Add([PSCustomObject]@{
                Message   = $Matches['Message']
                DateTime  = $dt
                Component = $Matches['Component']
                Type      = [int]$Matches['Type']
                Thread    = $Matches['Thread']
                File      = $Matches['File']
            })
        }
    }

    return $results
}

function Merge-LogFiles {
    <#
    .SYNOPSIS
        Merge multiple AppWorkload log files chronologically.
    #>
    param(
        [Parameter(Mandatory)]
        [string[]]$FilePaths
    )

    # Separate current file from rotated files
    # Handles both standard names (AppWorkload.log) and prefixed names (DeviceName_AppWorkload.log)
    $currentFile = $null
    $rotatedFiles = @()

    foreach ($fp in $FilePaths) {
        $name = [System.IO.Path]::GetFileNameWithoutExtension($fp)
        # Current file: ends with 'AppWorkload' (no timestamp suffix)
        if ($name -match '^(.*_)?AppWorkload$') {
            $currentFile = $fp
        }
        else {
            $rotatedFiles += $fp
        }
    }

    # Sort rotated files by timestamp suffix (oldest first)
    $rotatedFiles = $rotatedFiles | Sort-Object {
        $n = [System.IO.Path]::GetFileNameWithoutExtension($_)
        if ($n -match 'AppWorkload-(\d{8}-\d{6})') { $Matches[1] } else { $n }
    }

    # Build ordered file list: rotated (oldest->newest), then current
    $orderedFiles = @()
    $orderedFiles += $rotatedFiles
    if ($currentFile) { $orderedFiles += $currentFile }

    # Read and merge all content
    $allLines = [System.Collections.Generic.List[string]]::new()
    foreach ($f in $orderedFiles) {
        Write-Host "  Reading: $(Split-Path $f -Leaf)" -ForegroundColor Cyan
        $content = Get-Content -Path $f -Raw -ErrorAction SilentlyContinue
        if ($content) {
            $lines = $content -split "`r?`n"
            foreach ($l in $lines) {
                if ($l.Trim()) { $allLines.Add($l) }
            }
        }
    }

    return $allLines.ToArray()
}

